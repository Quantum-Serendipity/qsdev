package branding

import "sync/atomic"

type Config struct {
	AppName       string
	ConfigFile    string
	LocalConfig   string
	StateDir      string
	EnvLogVar     string
	EnvLogDirVar  string
	EnvNoUpdate   string
	EnvPrefix     string
	LogFilePrefix string
	TempPrefix    string
	GitHubOwner   string
	GitHubRepo    string
}

func Default() Config {
	return Config{
		AppName:       "qsdev",
		ConfigFile:    ".qsdev.yaml",
		LocalConfig:   ".qsdev.local.yaml",
		StateDir:      ".devinit",
		EnvLogVar:     "QSDEV_LOG",
		EnvLogDirVar:  "QSDEV_LOG_DIR",
		EnvNoUpdate:   "QSDEV_NO_UPDATE_CHECK",
		EnvPrefix:     "QSDEV_",
		LogFilePrefix: "qsdev-",
		TempPrefix:    ".qsdev-tmp-",
		GitHubOwner:   "Quantum-Serendipity",
		GitHubRepo:    "qsdev",
	}
}

var active atomic.Pointer[Config]

func init() {
	d := Default()
	active.Store(&d)
}

// Set merges non-empty fields from cfg into the active branding configuration.
// Must be called early in main(), before cmd.Main(). The gdev lockdown lifecycle
// prevents Set from being called after initialization.
func Set(cfg Config) {
	current := *active.Load()
	if cfg.AppName != "" {
		current.AppName = cfg.AppName
	}
	if cfg.ConfigFile != "" {
		current.ConfigFile = cfg.ConfigFile
	}
	if cfg.LocalConfig != "" {
		current.LocalConfig = cfg.LocalConfig
	}
	if cfg.StateDir != "" {
		current.StateDir = cfg.StateDir
	}
	if cfg.EnvLogVar != "" {
		current.EnvLogVar = cfg.EnvLogVar
	}
	if cfg.EnvLogDirVar != "" {
		current.EnvLogDirVar = cfg.EnvLogDirVar
	}
	if cfg.EnvNoUpdate != "" {
		current.EnvNoUpdate = cfg.EnvNoUpdate
	}
	if cfg.EnvPrefix != "" {
		current.EnvPrefix = cfg.EnvPrefix
	}
	if cfg.LogFilePrefix != "" {
		current.LogFilePrefix = cfg.LogFilePrefix
	}
	if cfg.TempPrefix != "" {
		current.TempPrefix = cfg.TempPrefix
	}
	if cfg.GitHubOwner != "" {
		current.GitHubOwner = cfg.GitHubOwner
	}
	if cfg.GitHubRepo != "" {
		current.GitHubRepo = cfg.GitHubRepo
	}
	active.Store(&current)
}

// Get returns the current branding configuration.
func Get() Config {
	return *active.Load()
}

// GeneratedBy returns a standard "Generated by <appname>" attribution string.
func GeneratedBy() string {
	return "Generated by " + Get().AppName
}
